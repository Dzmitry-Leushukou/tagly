def validate_posts():
    author_logins = {author["login"] for author in AUTHORS}
    if not (10 <= len(AUTHORS) <= 25):
        raise AssertionError(f"Ожидается 10-25 авторов, найдено {len(AUTHORS)}")

    if set(POSTS.keys()) != author_logins:
        missing = author_logins - set(POSTS.keys())
        extra = set(POSTS.keys()) - author_logins
        raise AssertionError(f"Несоответствие авторов POST данных. Пропущены: {missing}, лишние: {extra}")

    total_posts = sum(len(posts) for posts in POSTS.values())
    if total_posts != 100:
        raise AssertionError(f"Ожидается ровно 100 постов, найдено {total_posts}")

    for login, posts in POSTS.items():
        if len(posts) < 1:
            raise AssertionError(f"У автора {login} нет ни одного поста")
        for index, post in enumerate(posts, start=1):
            if not isinstance(post, str) or not post.strip():
                raise AssertionError(f"Пост #{index} автора {login} пуст или не строка")

    all_posts = [post.strip() for posts in POSTS.values() for post in posts]
    if len(all_posts) != len(set(all_posts)):
        raise AssertionError("Найдены дублирующиеся посты")

    for post in all_posts:
        text = post.lower()
        if not any(keyword in text for keyword in THEME_KEYWORDS):
            raise AssertionError(f"Пост не соответствует тематике технологий: {post[:80]}...")

    logger.info("Static data validation passed: %d authors, %d posts", len(AUTHORS), total_posts)

def seed_database():
    """Register users via Auth Service and create posts via PostService."""
    logger.info("Starting database seeding via services...")

    auth_url = os.getenv("AUTH_SERVICE_URL", AUTH_SERVICE_URL)
    post_url = os.getenv("POST_SERVICE_URL", POST_SERVICE_URL)

    # Step 1: Register all authors via Auth Service
    for author in AUTHORS:
        try:
            req = urllib.request.Request(
                f"{auth_url}/register",
                data=json.dumps({"login": author["login"], "password": author["password"]}).encode("utf-8"),
                headers={"Content-Type": "application/json"},
                method="POST",
            )
            with urllib.request.urlopen(req, timeout=10) as resp:
                result = json.loads(resp.read().decode("utf-8"))
                logger.info(f"Register {author['login']}: {result}")
        except urllib.error.HTTPError as e:
            body = e.read().decode("utf-8", errors="replace")
            logger.warning(f"Register {author['login']}: {e.code} {body}")
        except Exception as e:
            logger.warning(f"Register {author['login']} failed: {e}")

    # Step 2: Login each author and create posts via PostService
    for author in AUTHORS:
        login = author["login"]
        password = author["password"]
        posts = POSTS.get(login, [])

        token = None
        try:
            req = urllib.request.Request(
                f"{auth_url}/auth",
                data=json.dumps({"login": login, "password": password}).encode("utf-8"),
                headers={"Content-Type": "application/json"},
                method="POST",
            )
            with urllib.request.urlopen(req, timeout=10) as resp:
                data = json.loads(resp.read().decode("utf-8"))
                token = data.get("access_token")
                logger.info(f"Login {login}: {'OK' if token else 'No token'}")
        except urllib.error.HTTPError as e:
            body = e.read().decode("utf-8", errors="replace")
            logger.warning(f"Login {login}: {e.code} {body}")
            continue
        except Exception as e:
            logger.warning(f"Login {login} failed: {e}")
            continue

        if not token:
            continue

        for i, content in enumerate(posts):
            try:
                req = urllib.request.Request(
                    f"{post_url}/post",
                    data=json.dumps({"content": content}).encode("utf-8"),
                    headers={
                        "Content-Type": "application/json",
                        "Authorization": f"Bearer {token}",
                    },
                    method="POST",
                )
                with urllib.request.urlopen(req, timeout=60) as resp:
                    result = json.loads(resp.read().decode("utf-8"))
                    post_id = result.get("post_id", "?")
                    tags = result.get("tags", [])
                    logger.info(f"Post {i+1}/{len(posts)} by {login}: id={post_id}, tags={tags}")
                time.sleep(1)
            except urllib.error.HTTPError as e:
                body = e.read().decode("utf-8", errors="replace")
                logger.warning(f"Post {i+1} by {login}: {e.code} {body}")
            except Exception as e:
                logger.warning(f"Post {i+1} by {login} failed: {e}")

    logger.info("Database seeding complete!")

if __name__ == "__main__":
    logging.basicConfig(level=logging.INFO)
    validate_posts()
    seed_database()
