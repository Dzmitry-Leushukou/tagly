def validate_posts():
    authors_logins = [author["login"] for author in AUTHORS]
    if len(authors_logins) != len(set(authors_logins)):
        raise ValueError("Повторы логинов авторов обнаружены")

    if len(AUTHORS) != 10:
        raise ValueError(f"Должно быть 10 авторов, найдено {len(AUTHORS)}")

    for author in AUTHORS:
        login = author["login"]
        if login not in POSTS_BY_AUTHOR:
            raise ValueError(f"Автор {login} отсутствует в POSTS_BY_AUTHOR")
        if len(POSTS_BY_AUTHOR[login]) != 10:
            raise ValueError(f"У автора {login} должно быть 10 постов, найдено {len(POSTS_BY_AUTHOR[login])}")

    all_posts = []
    for posts in POSTS_BY_AUTHOR.values():
        all_posts.extend(posts)

    if len(all_posts) != 100:
        raise ValueError(f"Общее количество постов должно быть 100, найдено {len(all_posts)}")

    if len(set(all_posts)) != len(all_posts):
        duplicates = [post for post in set(all_posts) if all_posts.count(post) > 1]
        raise ValueError(f"Найдены дублирующиеся посты: {len(duplicates)}")

    logger.info("Авторов: %d", len(AUTHORS))
    logger.info("Постов всего: %d", len(all_posts))
    logger.info("Каждый автор имеет 10 постов")
    logger.info("Все посты уникальны")

def seed_database():
    logger.info("Starting database seeding...")

    auth_url = os.getenv("AUTH_SERVICE_URL", AUTH_SERVICE_URL)
    post_url = os.getenv("POST_SERVICE_URL", POST_SERVICE_URL)

    for author in AUTHORS:
        try:
            req = urllib.request.Request(
                f"{auth_url}/register",
                data=json.dumps({"login": author["login"], "password": author["password"]}).encode("utf-8"),
                headers={"Content-Type": "application/json"},
                method="POST",
            )
            with urllib.request.urlopen(req, timeout=10) as resp:
                result = json.loads(resp.read().decode("utf-8"))
                logger.info(f"Register {author['login']}: {result}")
        except urllib.error.HTTPError as e:
            body = e.read().decode("utf-8", errors="replace")
            logger.warning(f"Register {author['login']}: {e.code} {body}")
        except Exception as e:
            logger.warning(f"Register {author['login']} failed: {e}")

    for author in AUTHORS:
        login = author["login"]
        password = author["password"]
        posts = POSTS_BY_AUTHOR.get(login, [])

        token = None
        try:
            req = urllib.request.Request(
                f"{auth_url}/auth",
                data=json.dumps({"login": login, "password": password}).encode("utf-8"),
                headers={"Content-Type": "application/json"},
                method="POST",
            )
            with urllib.request.urlopen(req, timeout=10) as resp:
                data = json.loads(resp.read().decode("utf-8"))
                token = data.get("access_token")
                logger.info(f"Login {login}: OK")
        except urllib.error.HTTPError as e:
            body = e.read().decode("utf-8", errors="replace")
            logger.warning(f"Login {login}: {e.code} {body}")
            continue
        except Exception as e:
            logger.warning(f"Login {login} failed: {e}")
            continue

        if not token:
            continue

        for i, content in enumerate(posts):
            try:
                req = urllib.request.Request(
                    f"{post_url}/post",
                    data=json.dumps({"content": content}).encode("utf-8"),
                    headers={
                        "Content-Type": "application/json",
                        "Authorization": f"Bearer {token}",
                    },
                    method="POST",
                )
                with urllib.request.urlopen(req, timeout=60) as resp:
                    result = json.loads(resp.read().decode("utf-8"))
                    post_id = result.get("post_id", "?")
                    tags = result.get("tags", [])
                    logger.info(f"Post {i+1}/10 by {login}: id={post_id}")
                time.sleep(1)
            except urllib.error.HTTPError as e:
                body = e.read().decode("utf-8", errors="replace")
                logger.warning(f"Post {i+1} by {login}: {e.code}")
            except Exception as e:
                logger.warning(f"Post {i+1} by {login} failed: {e}")

    logger.info("Database seeding complete!")

