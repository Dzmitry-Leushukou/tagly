POSTS = POSTS_BY_AUTHOR


def validate_posts():
    authors_logins = [author["login"] for author in AUTHORS]
    if len(authors_logins) != len(set(authors_logins)):
        raise ValueError("Повторы логинов авторов обнаружены")

    if len(AUTHORS) < 10 or len(AUTHORS) > 25:
        raise ValueError("Количество авторов должно быть от 10 до 25")

    for author in AUTHORS:
        login = author["login"]
        if login not in POSTS_BY_AUTHOR:
            raise ValueError(f"Автор {login} отсутствует в POST_BY_AUTHOR")
        if not POSTS_BY_AUTHOR[login]:
            raise ValueError(f"У автора {login} нет постов")

    all_posts = []
    for posts in POSTS_BY_AUTHOR.values():
        all_posts.extend(posts)

    if len(all_posts) != 100:
        raise ValueError(f"Общее количество постов должно быть ровно 100, найдено {len(all_posts)}")

    if len(set(all_posts)) != len(all_posts):
        duplicates = [post for post in set(all_posts) if all_posts.count(post) > 1]
        raise ValueError(f"Найдены дублирующиеся посты: {len(duplicates)}")

    for idx, post in enumerate(all_posts, start=1):
        normalized = post.lower()
        if "ремонт" not in normalized or "квартир" not in normalized:
            raise ValueError(f"Пост {idx} не соответствует тематике ремонта квартиры")

    logging.info("Авторов: %d", len(AUTHORS))
    logging.info("Постов всего: %d", len(all_posts))
    logging.info("Каждый автор имеет хотя бы один пост")
    logging.info("Все посты уникальны")
    logging.info("Все посты соответствуют тематике 'Ремонт квартир'")

def seed_database():
    """Register users via Auth Service and create posts via PostService."""
    logger.info("Starting database seeding via services...")

    auth_url = os.getenv("AUTH_SERVICE_URL", AUTH_SERVICE_URL)
    post_url = os.getenv("POST_SERVICE_URL", POST_SERVICE_URL)

    # Step 1: Register all authors via Auth Service
    for author in AUTHORS:
        try:
            req = urllib.request.Request(
                f"{auth_url}/register",
                data=json.dumps({"login": author["login"], "password": author["password"]}).encode("utf-8"),
                headers={"Content-Type": "application/json"},
                method="POST",
            )
            with urllib.request.urlopen(req, timeout=10) as resp:
                result = json.loads(resp.read().decode("utf-8"))
                logger.info(f"Register {author['login']}: {result}")
        except urllib.error.HTTPError as e:
            body = e.read().decode("utf-8", errors="replace")
            logger.warning(f"Register {author['login']}: {e.code} {body}")
        except Exception as e:
            logger.warning(f"Register {author['login']} failed: {e}")

    # Step 2: Login each author and create posts via PostService
    for author in AUTHORS:
        login = author["login"]
        password = author["password"]
        posts = POSTS.get(login, [])

        token = None
        try:
            req = urllib.request.Request(
                f"{auth_url}/auth",
                data=json.dumps({"login": login, "password": password}).encode("utf-8"),
                headers={"Content-Type": "application/json"},
                method="POST",
            )
            with urllib.request.urlopen(req, timeout=10) as resp:
                data = json.loads(resp.read().decode("utf-8"))
                token = data.get("access_token")
                logger.info(f"Login {login}: {'OK' if token else 'No token'}")
        except urllib.error.HTTPError as e:
            body = e.read().decode("utf-8", errors="replace")
            logger.warning(f"Login {login}: {e.code} {body}")
            continue
        except Exception as e:
            logger.warning(f"Login {login} failed: {e}")
            continue

        if not token:
            continue

        for i, content in enumerate(posts):
            try:
                req = urllib.request.Request(
                    f"{post_url}/post",
                    data=json.dumps({"content": content}).encode("utf-8"),
                    headers={
                        "Content-Type": "application/json",
                        "Authorization": f"Bearer {token}",
                    },
                    method="POST",
                )
                with urllib.request.urlopen(req, timeout=60) as resp:
                    result = json.loads(resp.read().decode("utf-8"))
                    post_id = result.get("post_id", "?")
                    tags = result.get("tags", [])
                    logger.info(f"Post {i+1}/{len(posts)} by {login}: id={post_id}, tags={tags}")
                time.sleep(1)
            except urllib.error.HTTPError as e:
                body = e.read().decode("utf-8", errors="replace")
                logger.warning(f"Post {i+1} by {login}: {e.code} {body}")
            except Exception as e:
                logger.warning(f"Post {i+1} by {login} failed: {e}")

    logger.info("Database seeding complete!")

if __name__ == "__main__":
    logging.basicConfig(level=logging.INFO)
    validate_posts()
    seed_database()
